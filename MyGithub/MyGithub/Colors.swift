import Foundation

extension UIColor {
    
    class func githubGreenColor() -> UIColor {
        //HEX: #6cc644
        //rgb(108,198,68)
        return UIColor(red: 108.0 / 255.0, green: 198.0 / 255.0, blue: 68.0 / 255.0, alpha: 1.0)
    }
    
    class func githubRedColor() -> UIColor {
        
        return UIColor(red: 255.0 / 255.0, green: 42.0 / 255.0, blue: 52.0 / 255.0, alpha: 1.0)
        
    }
    
}